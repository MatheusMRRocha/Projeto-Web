            <p>Apesar de não haver nenhum anúncio formal de desenvolvimento, as palavras de Zampella reacendem a chama da esperança de que a narrativa envolvente e o gameplay único de Titanfall possam ter um terceiro capítulo no futuro.</p>
            <p>A comunidade especula sobre a história que poderia ser contada, talvez dando continuidade aos eventos de Titanfall 2 ou explorando novas perspectivas dentro do vasto universo da Fronteira.</p>
