            <p>Apesar do sucesso estrondoso de Apex Legends, que utiliza o mesmo universo de Titanfall, muitos fãs têm clamado por um retorno à experiência focada na campanha single-player e no multiplayer com Titãs.</p>
            <p>Zampella ressaltou que a Respawn possui uma paixão imensa pela franquia Titanfall e que a equipe de desenvolvimento adora o que eles criaram, sugerindo que o interesse interno por uma continuação ainda existe.</p>
            <p>A frase "na hora certa" tem sido interpretada pelos fãs como uma indicação de que a Respawn não quer apressar o projeto, aguardando o momento ideal em termos de recursos, tecnologia e visão criativa.</p>
